// ─── Helpers ────────────────────────────────────────────────────────

/** Índice por ID para búsqueda rápida */
const _spellById = new Map<string, SrdSpell>();
for (const s of SRD_SPELLS) _spellById.set(s.id, s);

/** Obtiene un hechizo por su ID */
export function getSpellById(id: string): SrdSpell | undefined {
  return _spellById.get(id);
}

/**
 * Devuelve los hechizos disponibles para una clase y nivel de hechizo.
 * Si no se pasa spellLevel, devuelve todos los de la clase.
 */
export function getSpellsForClass(
  classId: ClassId,
  spellLevel?: number,
): SrdSpell[] {
  return SRD_SPELLS.filter(
    (s) =>
      s.clases.includes(classId) &&
      (spellLevel === undefined || s.nivel === spellLevel),
  );
}

/**
 * Devuelve los hechizos disponibles para una clase hasta un nivel máximo de hechizo.
 * Útil para la selección al subir de nivel.
 */
export function getSpellsForClassUpToLevel(
  classId: ClassId,
  maxSpellLevel: number,
): SrdSpell[] {
  return SRD_SPELLS.filter(
    (s) =>
      s.clases.includes(classId) && s.nivel <= maxSpellLevel && s.nivel > 0,
  );
}

/**
 * Devuelve los trucos (nivel 0) disponibles para una clase.
 */
export function getCantripsForClass(classId: ClassId): SrdSpell[] {
  return SRD_SPELLS.filter((s) => s.clases.includes(classId) && s.nivel === 0);
}
